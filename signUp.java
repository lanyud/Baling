    private void initDatabase(){
        try{
            String url = "jdbc:mysql://localhost:3306/mysql";
            String user = "root";
            String password = "";
            
            conn = DriverManager.getConnection(url, user, password);
            if (conn != null){
                System.out.println("Connect to Database");
            }else{
                System.out.println("Failed connect to database");
            }
        } catch (SQLException e){
            System.out.println("SQL Exception" + e.getMessage());
        
        }
        
    }
    private void populateTable() {
    try {
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM account");

        DefaultTableModel model = (DefaultTableModel) TableListUser.getModel();
        model.setRowCount(0);
        Object[] row;
        while (rs.next()) {
            row = new Object[6];
            row[0] = rs.getString("firstname");
            row[1] = rs.getString("lastname");
            row[2] = rs.getString("email");
            row[3] = rs.getString("phone");
            row[4] = rs.getString("username" );
            row[5] = rs.getString("password");
            model.addRow(row);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    
            
