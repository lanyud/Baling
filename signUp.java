    private void ButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = TableListUser.getSelectedRow();
    
    if (selectedRow != -1) {
        String username = (String) TableListUser.getValueAt(selectedRow, 4);
        
        try {
            // Buat kueri SQL DELETE
            String sql = "DELETE FROM account WHERE username = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, username);
            int rowsDeleted = stmt.executeUpdate();
            
            // Hapus baris dari tabel
            DefaultTableModel dataModel = (DefaultTableModel) TableListUser.getModel();
            dataModel.removeRow(selectedRow);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
      
    }//GEN-LAST:event_ButtonDeleteActionPerformed

